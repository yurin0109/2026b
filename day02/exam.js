// =====================
// 자료형(리터럴)
// =====================

10                  // 숫자(Number) 리터럴
"유재석"             // 문자열(String) 리터럴
true                // 논리(Boolean) 리터럴
{}                  // 객체(Object)
[]                  // 배열(Array)
function fx(){}     // 함수(Function)
undefined           // 값이 아직 정의되지 않음
null                // 의도적으로 값이 없음을 나타냄

// =====================
// 출력 함수
// =====================

// console(콘솔)에 출력
console.log(10);

// =====================
// 변수
// =====================

// 변수란?
// 하나의 자료를 저장하는 메모리 공간

// 변수명은 같은 범위(scope)에서 중복 선언할 수 없다.
// 변수명은 카멜 표기법(myCar)을 권장한다.

let 이름상자1;          // 초기값 없음(undefined)
let 이름상자2 = 100;    // 변수 선언 + 초기값 대입

// 변수 값 출력
console.log(이름상자2);

// 변수 값 수정
이름상자2 = 200;
console.log(이름상자2);

// =====================
// 상수
// =====================

// 상수란?
// 값을 한 번 저장하면 수정할 수 없는 메모리 공간

const 이름상자3 = 300;

// 상수 값 출력
console.log(이름상자3);

// 이름상자3 = 500; // 오류(상수는 수정 불가)

// =====================
// 변수와 리터럴
// =====================

let point1 = 50;
