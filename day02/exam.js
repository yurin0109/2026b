// =====================
// 출력 함수
// =====================

// 1. console.log()
console.log("안녕하세요");

// 2. alert()
// 브라우저에 알림창 출력
alert("안녕하세요");

// 3. document.querySelector()
// HTML 요소를 선택하여 내용 변경
document.querySelector("h3").innerHTML = "JS에서 대입한 값";

// =====================
// 입력 함수
// =====================

// 1. confirm()
// 확인 = true
// 취소 = false

let 결과1 = confirm("계속하시겠습니까?");
console.log(결과1);

// 2. prompt()
// 입력한 값을 문자열(String)로 반환
// 취소하면 null 반환

let 결과2 = prompt("이름을 입력하세요.");
console.log(결과2);